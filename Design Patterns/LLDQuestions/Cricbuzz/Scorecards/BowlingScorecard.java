package LLDQuestions.Cricbuzz.Scorecards;

public class BowlingScorecard {
    private int oversBowled;
    private int wicketsTaken;
    private int noBallsBowled;
    private int widesBowled;
    private int runsGiven;
    private double economy;
    
    public int getOversBowled() {
        return oversBowled;
    }
    public void setOversBowled(int oversBowled) {
        this.oversBowled = oversBowled;
    }
    public int getWicketsTaken() {
        return wicketsTaken;
    }
    public void setWicketsTaken(int wicketsTaken) {
        this.wicketsTaken = wicketsTaken;
    }
    public int getNoBallsBowled() {
        return noBallsBowled;
    }
    public void setNoBallsBowled(int noBallsBowled) {
        this.noBallsBowled = noBallsBowled;
    }
    public int getWidesBowled() {
        return widesBowled;
    }
    public void setWidesBowled(int widesBowled) {
        this.widesBowled = widesBowled;
    }
    public int getRunsGiven() {
        return runsGiven;
    }
    public void setRunsGiven(int runsGiven) {
        this.runsGiven = runsGiven;
    }
    public double getEconomy() {
        return economy;
    }
    public void setEconomy(double economy) {
        this.economy = economy;
    }

    
}
